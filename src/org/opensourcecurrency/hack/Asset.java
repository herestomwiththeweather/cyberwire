package org.opensourcecurrency.hack;

public class Asset {
	public Integer assetId;
	public String url;
	public String name;
	
	public void setId(Integer id) {
		assetId = id;
	}
	
	public void setUrl(String asset_url) {
		url = asset_url;
	}
	
	public void setName(String asset_name) {
		name = asset_name;
	}
}
